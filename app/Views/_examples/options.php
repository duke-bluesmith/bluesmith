<h2>3D Printing Service</h2>

<h3>Services and Pricing</h3>
<section>
	<p>
		The cost of 3D printing varies widely and is based on technology and amount of
		material used.
	</p>
	<p>
		Self-service printing is available on our basic FDM printers with gray filament.
		Self-service printing is first-come first-served without charge.
		We do also allow some limited self-service SL printing.
	</p>
	<p>
		Premium service includes prioritization over all non-premium orders,
		support structure removal and model finishing, and pre and post-print
		consultation for each order.
	</p>
	<p>All premium jobs are also eligible for an additional post-processing inspection scan, available for an additional charge of $10.</p>
</section>

